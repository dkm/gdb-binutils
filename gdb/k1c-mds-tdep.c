static const char*
find_tdesc_arch (struct gdbarch *gdbarch)
{
	const struct target_desc *tdesc;

	if (gdbarch == NULL) return "k1c";
tdesc = gdbarch_target_desc (gdbarch);

	if (tdesc == NULL) return "k1c";
	if (tdesc_find_feature (tdesc, "eu.kalray.core.k1c"))
		return "k1c";
	return "k1c";
}

